package com.tpm.entity;

public class PracticeBook {
	private Integer practiceBookid;
	private String name;
	private String syllabus_Graid;
	public Integer getPracticeBookid() {
		return practiceBookid;
	}
	public void setPracticeBookid(Integer practiceBookid) {
		this.practiceBookid = practiceBookid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSyllabus_Graid() {
		return syllabus_Graid;
	}
	public void setSyllabus_Graid(String syllabus_Graid) {
		this.syllabus_Graid = syllabus_Graid;
	}

}
